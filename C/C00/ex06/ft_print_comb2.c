#include <unistd.h>

void	ft_print_comb2(void)
{
	int		i;
	int		j;
	char	n[5];

	i = 0;
	n[2] = 32;
	while (i < 99)
	{
		j = i + 1;
		while (j < 100)
		{
			n[0] = i / 10 + '0';
			n[1] = i % 10 + '0';
			n[3] = j / 10 + '0';
			n[4] = j % 10 + '0';
			write(1, n, 5);
			if (i != 98)
			{
				write(1, ", ", 2);
			}
			j++;
		}
		i++;
	}
}
