char	*ft_strlowcase(char *str)
{
	int	n;

	n = 0;
	while (str[n] != '\0')
	{
		if (str[n] > 64 && str[n] < 91)
		{
			str[n] = str[n] + 32;
		}
		n++;
	}
	return (str);
}
