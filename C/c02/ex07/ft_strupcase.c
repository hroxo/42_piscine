char	*ft_strupcase(char *str)
{
	int	n;

	n = 0;
	while (str[n] != '\0')
	{
		if (str[n] > 96 && str[n] < 123)
		{
			str[n] = str[n] - 32;
		}
		n++;
	}
	return (str);
}
