int	ft_str_is_numeric(char *str)
{
	int	n;

	n = 0;
	while (str[n] != '\0')
	{
		if (str[n] > 47 && str[n] < 58)
		{
			n++;
		}
		else
			return (0);
	}
	return (1);
}
