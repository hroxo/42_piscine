int	ft_str_is_alpha(char *str)
{
	int	n;

	n = 0;
	while (str[n] != '\0')
	{
		if (str[n] > 'a' - 1 && str[n] < 'z' + 1)
		{
			n++;
		}
		else if (str[n] > 64 && str[n] < 91)
		{
			n++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
