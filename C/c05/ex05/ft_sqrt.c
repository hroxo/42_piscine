int	ft_sqrt(int nb)
{
	int	nbr;

	nbr = 1;
	if (nb < 1)
		return (0);
	while (nbr * nbr <= nb)
	{
		nbr++;
	}
	nbr--;
	if (nbr * nbr == nb)
		return (nbr);
	return (0);
}
